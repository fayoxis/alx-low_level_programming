int add(int k, int i);
int sub(int j, int k);
int mul(int i, int j);
int div(int i, int k);
int mod(int k, int j);

/**
* add - this will  adds two integers
* @k: it is the first integer to add
* @i: it is the second integer to add
* Return: returns the sum
*/

int add(int k, int i)
{
return (k + i);
}

/**
* sub - this will subtracts two integers
* @j: this is the first integer to add
* @k: this is the second integer to add
* Return: returns the subtract
*/

int sub(int j, int k)
{
return (j - k);
}

/**
* mul - it multiplies two integers
* @i: this is the first integer to add
* @j: this is the second integer to add
* Return: returns the multiple
*/

int mul(int i, int j)
{
return (i * j);
}

/**
* div - it divides two integers
* @i: this is the first integer to add
* @k: this is the second integer to add
* Return: returns the divided integer
*/

int div(int i, int k)
{
return (i / k);
}

/**
 * mod - it finds the modulus of two integers
 * @k: this is the first integer to add
 * @j: this is the second integer to add
 * Return: returns the modulus
 */

int mod(int k, int j)
{
return (i % k);
}
