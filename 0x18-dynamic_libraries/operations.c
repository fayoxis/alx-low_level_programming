int add(int k, int i);
int sub(int j, int k);
int mul(int i, int j);
int div(int i, int k);
int mod(int k, int j);

/**
* add - adds two integers
* @k: first integer to add
* @i: second integer to add
* Return: the sum
*/

int add(int k, int i)
{
return (k + i);
}

/**
* sub - subtracts two integers
* @j: first integer to add
* @k: second integer to add
* Return: the subtract
*/

int sub(int j, int k)
{
return (j - k);
}

/**
* mul - multiplies two integers
* @i: first integer to add
* @j: second integer to add
* Return: the multiple
*/

int mul(int i, int j)
{
return (i * j);
}

/**
* div - divideds two integers
* @i: first integer to add
* @k: second integer to add
* Return: the dividend integer
*/

int div(int i, int k)
{
return (i / k);
}

/**
 * mod - finds the modulus of two integers
 * @k: first integer to add
 * @j: second integer to add
 * Return: the modulus
 */

int mod(int k, int j)
{
return (i % k);
}
